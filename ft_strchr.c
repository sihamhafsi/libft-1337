#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	x;

	x = c;
	while (*s != '\0' && *s != x)
		s++;
	if (*s == x)
		return ((char *) s);
	else if (!(*s == '\0'))
		return ((char *) s);
	return (NULL);
}
